--  FUNCTION TO CALCULATE EMPLOYEE
    FUNCTION CALC_EMP_SAL(SAL_DATE INVOICE_ORDER.ORDER_DATE%TYPE, E_ID EMPLOYEE.EMP_ID%TYPE) 
    RETURN NUMBER
    as
    TOTAL_SAL EMPLOYEE.SAL%TYPE;
    MONTH_NUMBER INT;
    YEAR_NUMBER INT;
    BEGIN 

    SELECT EXTRACT(MONTH FROM SAL_DATE) INTO MONTH_NUMBER FROM DUAL;
    SELECT EXTRACT(YEAR FROM SAL_DATE) INTO YEAR_NUMBER FROM DUAL;

    select ROUND(sum(invoice_order.sale_amt*0.03)+ EMPLOYEE.Sal,2) into TOTAL_SAL
    from employee, invoice_order
    where invoice_order.emp_id = employee.emp_id and extract(month from invoice_order.order_date) = MONTH_NUMBER
    and extract(year from invoice_order.order_date) = YEAR_NUMBER
    and employee.emp_id = E_ID
    group by employee.emp_id,sal;
    RETURN TOTAL_SAL;
    END CALC_EMP_SAL;

